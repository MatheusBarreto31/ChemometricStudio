    workspace_root = os.path.dirname(os.path.abspath(__file__))

    def _resolve_model_json_path() -> Path:
        env_path = os.environ.get("CMSTUDIO_MODEL_JSON_PATH", "").strip()
        if env_path:
            candidate = Path(env_path)
            if candidate.exists():
                return candidate

