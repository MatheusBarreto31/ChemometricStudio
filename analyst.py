    def _convert_param_types(base_alias, param_name, value, type_info):
        """Convert a parameter value to its specified type."""
        if value is None:
            return None
        
        param_type = type_info.get(param_name, "str")
        
        if param_type == "int":
            if isinstance(value, str):
                return int(value)
            elif isinstance(value, (int, float)):
                return int(value)
            else:
                return value
        elif param_type == "float":
            if isinstance(value, str):
                return float(value)
            elif isinstance(value, (int, float)):
                return float(value)
            else:
                return value
        elif param_type == "bool":
            if isinstance(value, bool):
                return value
            elif isinstance(value, str):
                return value.lower() in ('true', '1', 'yes')
            else:
                return bool(value)
        elif param_type == "list":
            if isinstance(value, list):
                return value
            elif isinstance(value, str):
                try:
                    parsed = ast.literal_eval(value)
                    return parsed if isinstance(parsed, list) else [parsed]
                except (ValueError, SyntaxError):
                    return [value]
            else:
                return [value] if value is not None else None
        else:
            # Default to string or passthrough
            return value
    
    outputs = {}  # {instance_alias: {param_key: value}}

    def _extract_nested(value: Any, nested_key: str):
        if not nested_key:
            return value, True
        current = value
        for part in str(nested_key).split('.'):
            if isinstance(current, dict) and part in current:
                current = current[part]
            else:
                return None, False
        return current, True

    def _resolve_routed_value(src_alias: str, src_param: str, nested_key: str, current_outputs: Dict[str, Dict[str, Any]]):
        if src_alias not in current_outputs:
            return None, False
        if src_param not in current_outputs[src_alias]:
            return None, False
        extracted, ok = _extract_nested(current_outputs[src_alias][src_param], nested_key)
        return extracted, ok

    def _apply_passforward_outputs(
        instance_alias: str,
        base_alias: str,
        function_outputs: Dict[str, Any],
        executed_params: Optional[Dict[str, Any]] = None,
    ) -> Dict[str, Any]:
        info = functions_info.get(instance_alias, {})
        if not bool(info.get('passforward_enabled', False)):
            return function_outputs

        passforward_spec = function_passforward_specs.get(base_alias, {})
        mappings = passforward_spec.get('mappings', {}) if isinstance(passforward_spec, dict) else {}
        if not isinstance(mappings, dict) or not mappings:
            return function_outputs

        params_source = executed_params if isinstance(executed_params, dict) else {}

        def _get_source_value(source_key: str) -> Any:
            if source_key in function_outputs:
                return function_outputs.get(source_key)
            return params_source.get(source_key)

        def _as_1d_array(value: Any) -> np.ndarray:
            arr = np.asarray(value)
            if arr.ndim == 0:
                return np.asarray([arr.item()])
            return arr.reshape(-1)

        def _build_score_axis_n_info(mapping: Dict[str, Any]) -> List[Any]:
            sample_source = str(mapping.get('sample_source', 'axis_n_info') or 'axis_n_info').strip()
            component_source = str(mapping.get('component_source', '') or '').strip()

            sample_axis = None
            candidate = _get_source_value(sample_source)
            if candidate is not None:
                if isinstance(candidate, (list, tuple)) and candidate:
                    sample_axis = candidate[0]
                else:
                    sample_axis = candidate

            component_axis = _get_source_value(component_source) if component_source else None

            if sample_axis is None:
                sample_axis = np.asarray([], dtype=float)
            if component_axis is None:
                component_axis = np.asarray([], dtype=float)

            return [np.asarray(sample_axis), np.asarray(component_axis)]

        def _build_score_axis_t_info(mapping: Dict[str, Any]) -> List[Any]:
            sample_text_source = str(mapping.get('sample_text_source', 'axis_t_info') or 'axis_t_info').strip()
            sample_axis_source = str(mapping.get('sample_axis_source', '') or '').strip()
            component_source = str(mapping.get('component_source', '') or '').strip()
            component_prefix = str(mapping.get('component_prefix', 'F') or 'F')

            sample_text: List[str] = []
            source_value = _get_source_value(sample_text_source)
            if isinstance(source_value, (list, tuple)) and source_value:
                sample_text = [str(v) for v in np.asarray(source_value[0]).reshape(-1)]

            if not sample_text:
                sample_axis_value = _get_source_value(sample_axis_source)
                if sample_axis_value is not None:
                    sample_text = [str(v) for v in _as_1d_array(sample_axis_value)]

            component_axis_value = _get_source_value(component_source)
            component_count = 0
            if component_axis_value is not None:
                component_count = int(_as_1d_array(component_axis_value).shape[0])
            factor_text = [f"{component_prefix}{idx + 1}" for idx in range(component_count)]
            return [sample_text, factor_text]

        def _build_score_dim_labels(mapping: Dict[str, Any]) -> List[str]:
            sample_label_source = str(mapping.get('sample_label_source', 'dim_labels') or 'dim_labels').strip()
            factor_label = str(mapping.get('factor_label', 'Factor') or 'Factor').strip() or 'Factor'

            sample_label = 'Samples'
            source_value = _get_source_value(sample_label_source)
            if isinstance(source_value, (list, tuple)) and source_value:
                maybe_label = str(source_value[0]).strip()
                if maybe_label:
                    sample_label = maybe_label
            return [sample_label, factor_label]

        for dst_key, mapping in mappings.items():
            if not isinstance(dst_key, str) or not isinstance(mapping, dict):
                continue

            builder = str(mapping.get('builder', '') or '').strip()
            if builder:
                try:
                    if builder == 'score_axis_n_info':
                        function_outputs[dst_key] = _build_score_axis_n_info(mapping)
                        print(f"  Passforward {builder} -> {dst_key}")
                        continue
                    if builder == 'score_axis_t_info':
                        function_outputs[dst_key] = _build_score_axis_t_info(mapping)
                        print(f"  Passforward {builder} -> {dst_key}")
                        continue
                    if builder == 'score_dim_labels':
                        function_outputs[dst_key] = _build_score_dim_labels(mapping)
                        print(f"  Passforward {builder} -> {dst_key}")
                        continue
                except Exception:
                    # Fall through to standard mapping behavior when builder fails.
                    pass

            if 'value' in mapping:
                function_outputs[dst_key] = mapping.get('value')
                print(f"  Passforward const -> {dst_key}")
                continue

            src_key = str(mapping.get('source', '')).strip()
            nested_key = str(mapping.get('nested_key', '')).strip()
            if not src_key or src_key not in function_outputs:
                continue

            extracted_value, ok = _extract_nested(function_outputs[src_key], nested_key)
            if not ok:
                continue

            function_outputs[dst_key] = extracted_value
            nested_suffix = f".{nested_key}" if nested_key else ""
            print(f"  Passforward {src_key}{nested_suffix} -> {dst_key}")

        return function_outputs

    def _find_matching_end(start_idx: int, start_alias: str, end_alias: str) -> int:
        depth = 0
        for idx in range(start_idx, len(functions_list)):
            base_alias = functions_list[idx]['base_alias']
            if base_alias == start_alias:
                depth += 1
            elif base_alias == end_alias:
                depth -= 1
                if depth == 0:
                    return idx
        return -1

    def _parse_sweep_values(raw_value: Any) -> List[Any]:
        if raw_value is None:
            return []

        if isinstance(raw_value, list):
            parsed_values: List[Any] = []
            for value in raw_value:
                text_value = str(value).strip()
                if not text_value:
                    continue
                try:
                    parsed_values.extend(parse_numeric_spec(text_value))
                except Exception:
                    parsed_values.append(text_value)
            return parsed_values

        text = str(raw_value).strip()
        if not text:
            return []

        parsed_values = []
        for part in [segment.strip() for segment in text.split(',') if segment.strip()]:
            try:
                parsed_values.extend(parse_numeric_spec(part))
            except Exception:
                parsed_values.append(part)
        return parsed_values

